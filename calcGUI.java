    private void calculateBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateBActionPerformed
            
        try {
            String date = jTextField1.getText();                                                    //collected all data from the fields and converting the ones needed in a differnet value
            double carKm = Double.parseDouble(jTextField2.getText());
            int busTrips = Integer.parseInt(jTextField3.getText());
            int flights = Integer.parseInt(jTextField4.getText());
            double houseSize = Double.parseDouble(jTextField5.getText());
            String dietType = jComboBox2.getSelectedItem().toString();
            String heatingType = jComboBox1.getSelectedItem().toString();

            //making personal emissions object
            currentEmission = new personalEmissions(0, date, carKm, busTrips, flights, dietType, houseSize, heatingType);
              // calculates annual emmisions
            double total = currentEmission.calculatorResult();
            
            //displays total , sumaray and level with details
            resultTA.setText("estimated annual CO2: " + total + " tonnes\n\n" + currentEmission.getEmissionSummary() + "\n"+ currentEmission.getEmissionCategory() + "\n\n"+ currentEmission.toString() );
    } catch (NumberFormatException e) {
            resultTA.setText("please enter number values.");//if user spells number
    } catch (Exception e) {
            resultTA.setText("error while calculating emissions: " + e.getMessage());//catche serror
    }
    }//GEN-LAST:event_calculateBActionPerformed

