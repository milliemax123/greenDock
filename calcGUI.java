    /*
    this loads all the users saved emission reports from the file and displays them in the text area
    */
    private void loadSavedReports() {
        ArrayList<personalEmissions> saved = handler.findAll();//gets all saved reports from file
        if (saved.isEmpty()) {
            savedReportsTA.setText("no reports saved yet");
            return;
        }
        StringBuilder sb = new StringBuilder(); //using a string builder to hold all personal emmisons that are saved 
        for (personalEmissions p : saved) {
            sb.append(p.toString()).append("\n\n");//adding each to sb
        }
        savedReportsTA.setText(sb.toString());
    }

